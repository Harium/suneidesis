package com.harium.suneidesis.knowledge;

public class Thing {

	protected String name;
	protected String id;

	public Thing(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
}
